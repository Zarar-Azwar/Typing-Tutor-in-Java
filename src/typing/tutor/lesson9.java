    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        if(currentchar==0){
            state=true;
            min.setEnabled(true);
            sec.setEnabled(true);
            hour.setEnabled(true);
            mili.setEnabled(true);
            time();
        }
        if(evt.getKeyChar()==lesson1.charAt(currentchar)){
            label.get(currentchar).setForeground(Color.GREEN);
            label.get(currentchar).setBorder(null);
            char ch= evt.getKeyChar();
            int f=q.indexOf(Character.toString(ch));
            setButtonColor(ch,f);
            currentchar++;
            if(currentchar>50){
                jTextField1.setEditable(false);
            }
        }
        else{
            label.get(currentchar).setForeground(Color.RED);
            label.get(currentchar).setBorder(null);
            char ch= evt.getKeyChar();
            int f=q.indexOf(Character.toString(ch));
            setButtonColor(ch,f);
            currentchar++;
            error++;
            if(currentchar>50){
                jTextField1.setEditable(false);
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

